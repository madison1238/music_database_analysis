CREATE DATABASE IF NOT EXISTS DB_FINAL;
USE DB_FINAL;

SET FOREIGN_KEY_CHECKS=0;
Drop table IF EXISTS ACCOUNT;
drop table IF EXISTS LISTENER;
drop table IF EXISTS SUBSCRIPTION_PLAN;
drop table IF EXISTS PAYMENT_METHOD;
drop table IF EXISTS BILLING_EVENT;
drop table IF EXISTS ARTIST;
drop table IF EXISTS ALBUM;
drop table IF EXISTS SONG;
drop table IF EXISTS LISTENING_EVENT;
drop table IF EXISTS PLAYLIST;
drop table IF EXISTS PLAYLIST_SONG;
SET FOREIGN_KEY_CHECKS=1;


CREATE TABLE ACCOUNT( 
ACCOUNT_ID INTEGER NOT NULL,
FULL_NAME VARCHAR(100),
JOIN_DATE DATE,
CONSTRAINT ACCOUNT_PK PRIMARY KEY(ACCOUNT_ID) );

CREATE TABLE LISTENER ( 
LISTENER_ID INTEGER NOT NULL,
CONSTRAINT LISTENER_PK PRIMARY KEY(LISTENER_ID),
CONSTRAINT LISTENER_FK FOREIGN KEY(LISTENER_ID) REFERENCES ACCOUNT (ACCOUNT_ID));

CREATE TABLE SUBSCRIPTION_PLAN (
PLAN_ID INTEGER NOT NULL,
PLAN_NAME VARCHAR(50),
MONTHLY_PRICE DECIMAL(3,2),
CONSTRAINT SUBSCRIPTION_PLAN_PK PRIMARY KEY (PLAN_ID));

CREATE TABLE PAYMENT_METHOD (
PAYMENT_METHOD_ID INTEGER NOT NULL,
METHOD VARCHAR(50),
CONSTRAINT PAYMENT_METHOD_PK PRIMARY KEY (PAYMENT_METHOD_ID));

CREATE TABLE BILLING_EVENT (
PLAN_ID INTEGER NOT NULL,
PAYMENT_METHOD_ID INTEGER NOT NULL,
LISTENER_ID INTEGER NOT NULL,
DATE_PAID DATE NOT NULL,
CONSTRAINT BILLING_EVENT_PK PRIMARY KEY (PLAN_ID, PAYMENT_METHOD_ID, LISTENER_ID, DATE_PAID));



CREATE TABLE ARTIST (
ARTIST_ID INTEGER NOT NULL,
GENRE VARCHAR(50),
COUNTRY VARCHAR(100),
CONSTRAINT ARTIST_PK PRIMARY KEY(ARTIST_ID),
CONSTRAINT ARTIST_FK FOREIGN KEY(ARTIST_ID) REFERENCES ACCOUNT (ACCOUNT_ID));

CREATE TABLE ALBUM (
ALBUM_ID INTEGER NOT NULL,
ARTIST_ID INTEGER ,
TITLE VARCHAR(100),
RELEASE_DATE DATE,
CONSTRAINT ALBUM_PK PRIMARY KEY (ALBUM_ID),
CONSTRAINT ALBUM_FK FOREIGN KEY(ARTIST_ID) REFERENCES ARTIST (ARTIST_ID));

CREATE TABLE SONG (
SONG_ID INTEGER NOT NULL,
ALBUM_ID INTEGER,
ARTIST_ID INTEGER,
TITLE VARCHAR(100),
DURATION INTEGER,
ORDER_NUMBER INTEGER,
CONSTRAINT SONG_PK PRIMARY KEY (SONG_ID),
CONSTRAINT SONG_FK1 FOREIGN KEY (ALBUM_ID) REFERENCES ALBUM (ALBUM_ID),
CONSTRAINT SONG_FK2 FOREIGN KEY (ARTIST_ID) REFERENCES ARTIST (ARTIST_ID));

CREATE TABLE LISTENING_EVENT (
LISTENING_EVENT_ID INTEGER NOT NULL,
LISTENER_ID INTEGER,
SONG_ID INTEGER,
DATE_LISTENED DATE,
CONSTRAINT LISTENING_EVENT_PK PRIMARY KEY (LISTENING_EVENT_ID),
CONSTRAINT LISTENING_EVENT_FK1 FOREIGN KEY (LISTENER_ID) REFERENCES LISTENER (LISTENER_ID),
CONSTRAINT LISTENING_EVENT_FK2 FOREIGN KEY (SONG_ID) REFERENCES SONG (SONG_ID));

CREATE TABLE PLAYLIST (
PLAYLIST_ID INTEGER NOT NULL,
LISTENER_ID INTEGER,
TITLE VARCHAR(50),
CONSTRAINT PLAYLIST_PK PRIMARY KEY (PLAYLIST_ID),
CONSTRAINT PLAYLIST_FK FOREIGN KEY (LISTENER_ID) REFERENCES LISTENER (LISTENER_ID));

CREATE TABLE PLAYLIST_SONG (
PLAYLIST_ID INTEGER NOT NULL,
SONG_ID INTEGER NOT NULL,
ORDER_NUMBER INTEGER,
DATE_ADDED DATE,
CONSTRAINT PLAYLIST_SONG_PK PRIMARY KEY (PLAYLIST_ID, SONG_ID),
CONSTRAINT PLAYLIST_SONG_FK1 FOREIGN KEY (PLAYLIST_ID) REFERENCES PLAYLIST (PLAYLIST_ID),
CONSTRAINT PLAYLIST_SONG_FK2 FOREIGN KEY (SONG_ID) REFERENCES SONG (SONG_ID));





